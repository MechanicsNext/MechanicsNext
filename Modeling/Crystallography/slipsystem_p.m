% Get b and m for 12 plastic slip systems
function [b0,m0]=slipsystem_p(nslip)
if(nslip==12)
    m0(1:3,1)=[0  1  0];            b0(1:3,1)= [1  0  0];
    m0(1:3,2)=[0  0  1];            b0(1:3,2)= [1  0  0];
    m0(1:3,3)=[1  0  0];            b0(1:3,3)= [0  1  0];
    m0(1:3,4)=[0  0  1];            b0(1:3,4)= [0  1  0];
    m0(1:3,5)=[1  0  0];            b0(1:3,5)= [0  0  1];
    m0(1:3,6)=[0  1  0];            b0(1:3,6)= [0  0  1];
    m0(1:3,7)=[0  1  1]/sqrt(2);    b0(1:3,7)= [1  0  0];
    m0(1:3,8)=[0  1 -1]/sqrt(2);    b0(1:3,8)= [1  0  0];
    m0(1:3,9)=[1  0  1]/sqrt(2);    b0(1:3,9)= [0  1  0];
    m0(1:3,10)=[1  0 -1]/sqrt(2);   b0(1:3,10)=[0  1  0];
    m0(1:3,11)=[1  1  0]/sqrt(2);   b0(1:3,11)=[0  0  1];
    m0(1:3,12)=[-1  1  0]/sqrt(2);  b0(1:3,12)=[0  0  1];
end
% Get only 6 sl syst of <100>{011} type 
if(nslip==6)
    m0(1:3,1)=[0  1  1]/sqrt(2);    b0(1:3,1)= [1  0  0];
    m0(1:3,2)=[0  1 -1]/sqrt(2);    b0(1:3,2)= [1  0  0];
    m0(1:3,3)=[1  0  1]/sqrt(2);    b0(1:3,3)= [0  1  0];
    m0(1:3,4)=[1  0 -1]/sqrt(2);   b0(1:3,4)=[0  1  0];
    m0(1:3,5)=[1  1  0]/sqrt(2);   b0(1:3,5)=[0  0  1];
    m0(1:3,6)=[-1  1  0]/sqrt(2);  b0(1:3,6)=[0  0  1];
end
end
